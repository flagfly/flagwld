enum http_parser_url_fields
  { UF_SCHEMA           = 0
  , UF_HOST             = 1
  , UF_PORT             = 2
  , UF_PATH             = 3
  , UF_QUERY            = 4
  , UF_FRAGMENT         = 5
  , UF_USERINFO         = 6
  , UF_MAX              = 7
  };

/* Result structure for http_parser_parse_url().
 *
 * Callers should index into field_data[] with UF_* values iff field_set
 * has the relevant (1 << UF_*) bit set. As a courtesy to clients (and
 * because we probably have padding left over), we convert any port to
 * a uint16_t.
 */
struct http_parser_url {
  uint16_t field_set;           /* Bitmask of (1 << UF_*) values */
  uint16_t port;                /* Converted UF_PORT string */

  struct {
    uint16_t off;               /* Offset into buffer in which field starts */
    uint16_t len;               /* Length of run in buffer */
  } field_data[UF_MAX];
};

/* Parse a URL; return nonzero on failure */
static int http_parser_parse_url(const char *buf, size_t buflen,
                          int is_connect,
                          struct http_parser_url *u);

static void dump_url (const char *url, const struct http_parser_url *u);

UrlParser::UrlParser():port_(0)
{
}
UrlParser::~UrlParser()
{
}
bool UrlParser::Execute(const char *buf, size_t buflen)
{
  http_parser_url u;

  if (http_parser_parse_url(buf, buflen, 0, &u)) {
    return false;
  }

  //dump_url(buf, &u);

  if (u.field_set & (1 << UF_SCHEMA)) {
    proto_.assign(buf+u.field_data[UF_SCHEMA].off, u.field_data[UF_SCHEMA].len);
  }
  if (u.field_set & (1 << UF_HOST)) {
    host_.assign(buf+u.field_data[UF_HOST].off, u.field_data[UF_HOST].len);
  }
  if (u.field_set & (1 << UF_PORT)) {
    port_ = u.port;
  }
  if (u.field_set & (1 << UF_PATH)) {
    path_.assign(buf+u.field_data[UF_PATH].off, u.field_data[UF_PATH].len);
  }
  if (u.field_set & (1 << UF_QUERY)) {
    query_.assign(buf+u.field_data[UF_QUERY].off, u.field_data[UF_QUERY].len);
  }
  if (u.field_set & (1 << UF_FRAGMENT)) {
    fragment_.assign(buf+u.field_data[UF_FRAGMENT].off, u.field_data[UF_FRAGMENT].len);
  }
  if (u.field_set & (1 << UF_USERINFO)) {
    usrinfo_.assign(buf+u.field_data[UF_USERINFO].off, u.field_data[UF_USERINFO].len);
  }

  return true;
}


